public class A050Operation {
    public static void main(String[] args){
        //数字与数字相加的结果是求和
        System.out.println( 10 + 20 );
        //数字与字符串相加是拼接
        System.out.println( 10 + "20" );

        int a = 10;
        int b = 20;
        //动态的输出a+b
        System.out.println(a + "+" + b + "=" +(a + b));
    }
}
