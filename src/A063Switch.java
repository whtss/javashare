public class A063Switch {
    public static void main(String[] args) {
        //char c = 'A';
        char c = 66;
        switch (c){
            case 'A':
                System.out.println("高级");break;
            case 'B':
                System.out.println("中级");break;
            case 'C':
                System.out.println("低级");break;
        }
    }
}
